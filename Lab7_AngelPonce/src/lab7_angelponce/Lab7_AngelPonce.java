    private void jButton16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton16MouseClicked
        if (jla.getSelectedIndex() >= 0) {
            asactual = astronautas.get(jla.getSelectedIndex());
            if (pasajeros < naveactual.getCantidadmaximapersonas()) {
                if (asactual.getTitulouniversitario().equals("Ingeniero")) {
                    ing++;
                } else if (asactual.getTitulouniversitario().equals("Fisico")) {
                    fis++;
                } else if (asactual.getTitulouniversitario().equals("Astrofisico")) {
                    ast++;
                }
                JOptionPane.showMessageDialog(null, "Se agregÃ³ pasajero a la nave");
                pasajeros++;
                correcto++;
            } else {
                JOptionPane.showMessageDialog(null, "Ya no caben mas pasajeros en esa nave");
            }
        }
    }//GEN-LAST:event_jButton16MouseClicked

