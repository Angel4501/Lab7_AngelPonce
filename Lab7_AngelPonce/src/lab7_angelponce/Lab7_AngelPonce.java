    private void jButton18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton18MouseClicked
        if (gasolina.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar velocidad a la que ira la nave");
        } else {
            if (correcto > 3) {
                boolean entrar = true;
                if (ing < 1 || fis < 1 || ast < 1) {
                    JOptionPane.showMessageDialog(null, "Debe ir minimo un Ingeniero, un Fisico, y un Astrofisico");
                    entrar = false;
                }
                if (Integer.parseInt(velocidad1.getText()) > naveactual.getVelocidad()) {
                    JOptionPane.showMessageDialog(null, "Debe seleccionar otra velocidad, menor a la de esa nave");
                    entrar = false;
                }
                if(gasolina.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Debe ingresar cuanta gasolina tiene el tanque actualmente");
                    entrar=false;
                }
                else if(!gasolina.getText().equals("")){
                    if(Integer.parseInt(gasolina.getText())<0){
                        JOptionPane.showMessageDialog(null, "Gasolina no puede ser menor a 0");
                        entrar=false;
                    }
                }
                if (pactual.getDistancia() > naveactual.getDistanciamaxima()) {
                    JOptionPane.showMessageDialog(null, "El planeta estÃ¡ muy lejos para la distancia maxima de esa nave");
                    entrar = false;
                }
                if (entrar == true) {
                    c = new ClaseHilo(pbsimulacion,naveactual.getCantidadcombustible(),Integer.parseInt(velocidad1.getText())
                    ,pactual.getDistancia(),Integer.parseInt(gasolina.getText()));
                    c.start();
                    simulacion.pack();
                    simulacion.setModal(true);
                    simulacion.setLocationRelativeTo(this);
                    simulacion.setVisible(true);
                    correcto = 0;
                    pasajeros = 0;
                    jButton15.setEnabled(true);
                    jButton17.setEnabled(true);
                    
                    
                }
            } else {
                JOptionPane.showMessageDialog(null, "Aun no puede hacer simulacion");
            }

        }

    }//GEN-LAST:event_jButton18MouseClicked

